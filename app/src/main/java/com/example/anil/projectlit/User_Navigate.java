    @RequiresApi(api = Build.VERSION_CODES.GINGERBREAD)
    @SuppressWarnings("StatementWithEmptyBody")
    @Override
    public boolean onNavigationItemSelected(MenuItem item) {
        // Handle navigation view item clicks here.
        int id = item.getItemId();

        if (id == R.id.nav_manage) {
            Intent i = new Intent(getApplicationContext(), ControlPane.class);
            startActivity(i);
        }
        else if (id == R.id.nav_gallery)
        {
            Intent i = new Intent(getApplicationContext(), Scheduler.class);
            startActivity(i);
        }
        else if(id==R.id.nav_slideshow)
        {
            Intent i = new Intent(getApplicationContext(), UnitHis.class);
            startActivity(i);
        }
        else if(id==R.id.logout)
        {
            FirebaseAuth.getInstance().signOut();
            Intent i = new Intent(getApplicationContext(), Login.class);
            startActivity(i);
        }
        else if(id==R.id.nav_send){
            Intent phoneIntent = new Intent(Intent.ACTION_CALL);
            phoneIntent.setData(Uri.parse("tel:91-866-912-5072"));



        }



        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        drawer.closeDrawer(GravityCompat.START);
        return true;
    }
